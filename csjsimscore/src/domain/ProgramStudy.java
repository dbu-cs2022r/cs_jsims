package domain;

public class ProgramStudy {

    int id;
    int fieldOfStudyId;
    int programAwardId;
    int programAdmissionId;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getFieldOfStudyId() {
        return fieldOfStudyId;
    }

    public void setFieldOfStudyId(int fieldOfStudyId) {
        this.fieldOfStudyId = fieldOfStudyId;
    }

    public int getProgramAwardId() {
        return programAwardId;
    }

    public void setProgramAwardId(int programAwardId) {
        this.programAwardId = programAwardId;
    }

    public int getProgramAdmissionId() {
        return programAdmissionId;
    }

    public void setProgramAdmissionId(int programAdmissionId) {
        this.programAdmissionId = programAdmissionId;
    }

}
