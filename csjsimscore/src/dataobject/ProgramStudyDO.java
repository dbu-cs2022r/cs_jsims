package dataobject;

public class ProgramStudyDO {

    int programStudyId;
    String fieldOfStudy;
    int programAwardId;
    String programAward;
    int programAdmissionId;
    String programAdmission;

    public int getProgramStudyId() {
        return programStudyId;
    }

    public void setProgramStudyId(int programStudyId) {
        this.programStudyId = programStudyId;
    }

    public String getFieldOfStudy() {
        return fieldOfStudy;
    }

    public void setFieldOfStudy(String fieldOfStudy) {
        this.fieldOfStudy = fieldOfStudy;
    }

    public int getProgramAwardId() {
        return programAwardId;
    }

    public void setProgramAwardId(int programAwardId) {
        this.programAwardId = programAwardId;
    }

    public String getProgramAward() {
        return programAward;
    }

    public void setProgramAward(String programAward) {
        this.programAward = programAward;
    }

    public int getProgramAdmissionId() {
        return programAdmissionId;
    }

    public void setProgramAdmissionId(int programAdmissionId) {
        this.programAdmissionId = programAdmissionId;
    }

    public String getProgramAdmission() {
        return programAdmission;
    }

    public void setProgramAdmission(String programAdmission) {
        this.programAdmission = programAdmission;
    }

}
